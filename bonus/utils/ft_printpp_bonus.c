#include "Cub3d_bonus.h"

void	ft_printpp(char **pp)
{
	int	j;

	j = 0;
	if (pp != NULL)
	{
		while (pp[j])
		{
			printf("pp[%d]: %s|\n", j, pp[j]);
			j++;
		}
	}
}
